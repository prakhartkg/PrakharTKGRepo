package com.yash.builder.subway.bread;

public class Bread {
	private boolean tosted;
	private int size;
	private boolean cheased;
	public boolean isTosted() {
		return tosted;
	}
	public void setTosted(boolean tosted) {
		this.tosted = tosted;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public boolean isCheased() {
		return cheased;
	}
	public void setCheased(boolean cheased) {
		this.cheased = cheased;
	}
	
}
